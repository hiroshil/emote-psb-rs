#[derive(Parser, Debug)]
#[command(
    name = "emote-psb",
    version,
    about = "Inspect, dump, pack, unpack, convert, and repack E-mote PSB/MDF files."
)]
struct Cli {
    #[command(subcommand)]
    command: Command,
}

#[derive(Subcommand, Debug)]
enum Command {
    /// Show high-level file metadata.
    Info {
        input: PathBuf,

        #[arg(long, value_enum, default_value_t = InputFormat::Auto)]
        format: InputFormat,

        /// Emit machine-readable JSON.
        #[arg(long)]
        json: bool,

        #[command(flatten)]
        mdf: MdfCliOptions,
    },

    /// Dump the PSB root object as JSON.
    Dump {
        input: PathBuf,

        #[arg(long, value_enum, default_value_t = InputFormat::Auto)]
        format: InputFormat,

        /// Output JSON path. Prints to stdout when omitted.
        #[arg(short, long)]
        output: Option<PathBuf>,

        /// Pretty-print JSON.
        #[arg(long)]
        pretty: bool,

        #[command(flatten)]
        mdf: MdfCliOptions,
    },

    /// Build a PSB file from a root JSON file and optional resource files.
    Build {
        root_json: PathBuf,
        output: PathBuf,

        /// PSB version. Valid values are 2, 3, and 4.
        #[arg(long, default_value_t = 3)]
        version: u16,

        /// Mark output PSB as encrypted.
        #[arg(long)]
        encrypted: bool,

        /// Add normal resource file. Repeat to preserve indices.
        #[arg(long = "resource", value_name = "FILE")]
        resources: Vec<PathBuf>,

        /// Add extra resource file. Repeat to preserve indices. Requires version 4.
        #[arg(long = "extra", value_name = "FILE")]
        extras: Vec<PathBuf>,
    },

    /// Repack a PSB/MDF into a normalized PSB while preserving root and resources.
    Repack {
        input: PathBuf,
        output: PathBuf,

        #[arg(long, value_enum, default_value_t = InputFormat::Auto)]
        format: InputFormat,

        /// Override output PSB version. Defaults to source version.
        #[arg(long)]
        version: Option<u16>,

        /// Override output encrypted flag.
        #[arg(long, value_name = "BOOL")]
        encrypted: Option<bool>,

        #[command(flatten)]
        mdf: MdfCliOptions,
    },

    /// Unpack PSB/MDF into root JSON, resources, extra resources, and manifest JSON.
    Unpack {
        input: PathBuf,
        out_dir: PathBuf,

        #[arg(long, value_enum, default_value_t = InputFormat::Auto)]
        format: InputFormat,

        /// Overwrite an existing non-empty output directory.
        #[arg(long)]
        overwrite: bool,

        /// Pretty-print generated root.json and manifest.json.
        #[arg(long)]
        pretty: bool,

        #[command(flatten)]
        mdf: MdfCliOptions,
    },

    /// Pack a PSB from a manifest JSON generated by `unpack`.
    Pack {
        manifest_json: PathBuf,
        output: PathBuf,

        /// Override manifest root JSON path.
        #[arg(long)]
        root: Option<PathBuf>,

        /// Override manifest resources. Repeat to preserve indices.
        #[arg(long = "resource", value_name = "FILE")]
        resources: Vec<PathBuf>,

        /// Override manifest extra resources. Repeat to preserve indices. Requires version 4.
        #[arg(long = "extra", value_name = "FILE")]
        extras: Vec<PathBuf>,

        /// Override manifest version.
        #[arg(long)]
        version: Option<u16>,

        /// Override manifest encrypted flag.
        #[arg(long, value_name = "BOOL")]
        encrypted: Option<bool>,
    },

    /// Decode an MDF wrapper to raw PSB bytes.
    DecodeMdf {
        input: PathBuf,
        output: PathBuf,

        #[command(flatten)]
        mdf: MdfCliOptions,
    },

    /// Encode raw PSB bytes into an MDF wrapper.
    EncodeMdf {
        input: PathBuf,
        output: PathBuf,

        /// zlib compression level passed to MdfWriter.
        #[arg(long, default_value_t = 6)]
        level: u32,
    },

    /// Convert PSB <-> MDF. With `auto`, the target format is inferred from output extension.
    Convert {
        input: PathBuf,
        output: PathBuf,

        #[arg(long = "from", value_enum, default_value_t = InputFormat::Auto)]
        from_format: InputFormat,

        #[arg(long = "to", value_enum, default_value_t = OutputFormat::Auto)]
        to_format: OutputFormat,

        /// zlib compression level used when writing MDF.
        #[arg(long, default_value_t = 6)]
        level: u32,

        #[command(flatten)]
        mdf: MdfCliOptions,
    },

    /// Inspect an MArchive pair. Pass the `*_info.psb.m`; the matching `*_body.bin` is inferred.
    ArchiveInfo {
        /// Archive info PSB/MDF file, for example scenario_info.psb.m.
        info: PathBuf,

        /// Archive body file. Defaults to `{prefix}_body.bin` next to `{prefix}_info.psb.m`.
        #[arg(long)]
        body: Option<PathBuf>,

        /// Output JSON report path. Prints to stdout when omitted.
        #[arg(short, long)]
        output: Option<PathBuf>,

        /// Pretty-print JSON.
        #[arg(long)]
        pretty: bool,

        /// Decode each MDF body slice and validate the decoded PSB signature.
        #[arg(long)]
        deep: bool,

        #[command(flatten)]
        mdf: MdfCliOptions,
    },

    /// Extract all slices from an MArchive using the decoded info manifest.
    ArchiveUnpack {
        /// Archive info PSB/MDF file, for example scenario_info.psb.m. The body defaults to scenario_body.bin in the same directory.
        info: PathBuf,

        /// Output directory. By default, writes editable files directly under this directory using original archive names.
        out_dir: PathBuf,

        /// Archive body file. Defaults to `{prefix}_body.bin` next to `{prefix}_info.psb.m`.
        #[arg(long)]
        body: Option<PathBuf>,

        /// Overwrite an existing non-empty output directory.
        #[arg(long)]
        overwrite: bool,

        /// Pretty-print generated JSON files.
        #[arg(long)]
        pretty: bool,

        /// Also write the original body slices before MDF decoding.
        #[arg(long)]
        raw_slices: bool,

        /// Compatibility no-op: named entries are now written by default.
        #[arg(long, hide = true, action = ArgAction::SetTrue)]
        named_entries: bool,

        /// Also write legacy numbered decoded entries, for example entries/0038_startup.bin. Disabled by default.
        #[arg(long = "indexed-entries")]
        indexed_entries: bool,

        /// Abort on the first entry-level extraction error.
        #[arg(long)]
        strict: bool,

        #[command(flatten)]
        mdf: MdfCliOptions,
    },

    /// Repack an MArchive pair after editing files extracted by `archive-unpack`.
    RepackArchive {
        /// Original archive info PSB/MDF file, for example scenario_info.psb.m. The original body defaults to scenario_body.bin in the same directory.
        info: PathBuf,

        /// Directory produced by `archive-unpack`, or a FreeMote-like source directory.
        in_dir: PathBuf,

        /// Output archive info PSB/MDF file. The output body defaults to the matching `*_body.bin` next to this path.
        out_info: PathBuf,

        /// Original archive body file. Defaults to `{prefix}_body.bin` next to the input info file.
        #[arg(long)]
        body: Option<PathBuf>,

        /// Output archive body file. Defaults to `{prefix}_body.bin` next to the output info file.
        #[arg(long = "out-body")]
        out_body: Option<PathBuf>,

        /// Output format for out_info. Auto treats `.psb.m`, `.m`, and `.mdf` as MDF.
        #[arg(long = "info-format", value_enum, default_value_t = OutputFormat::Auto)]
        info_format: OutputFormat,

        /// zlib compression level used for rewritten MDF body slices and MDF info output.
        #[arg(long, default_value_t = 6)]
        level: u32,

        /// Output shell for rewritten MDF body slices.
        #[arg(long = "body-mdf-shell", value_enum, default_value_t = MdfWriteShell::Auto)]
        body_mdf_shell: MdfWriteShell,

        /// Output shell for out_info when it is MDF.
        #[arg(long = "info-mdf-shell", value_enum, default_value_t = MdfWriteShell::Auto)]
        info_mdf_shell: MdfWriteShell,

        /// MT19937 seed variant used when writing MT19937 MDF.
        #[arg(long = "mt-seed-variant", value_enum, default_value_t = MtSeedVariantCli::Md5ArrayLe)]
        mt_seed_variant: MtSeedVariantCli,

        /// MT19937 XOR key variant used when writing MT19937 MDF.
        #[arg(long = "mt-key-variant", value_enum, default_value_t = MtKeyVariantCli::CyclicLeWords)]
        mt_key_variant: MtKeyVariantCli,

        /// Preserve original raw body slice when no replacement is found.
        #[arg(long, default_value_t = true)]
        preserve_missing: bool,

        /// Abort if an entry has no replacement in in_dir.
        #[arg(long)]
        strict: bool,

        /// Prefer raw_slices/*.slice over decoded entries/*.psb or entries/*.bin.
        #[arg(long)]
        prefer_raw_slices: bool,

        /// Preserve source offset order. Use `--free-mote-order` to sort by archive name.
        #[arg(long = "free-mote-order")]
        free_mote_order: bool,

        /// Override the output MDF seed name for out_info. Defaults to out_info file name.
        #[arg(long = "info-mdf-name")]
        info_mdf_name: Option<String>,

        #[command(flatten)]
        mdf: MdfCliOptions,
    },
}

#[derive(Clone, Copy, Debug, ValueEnum)]
enum InputFormat {
    Auto,
    Psb,
    Mdf,
}

#[derive(Clone, Copy, Debug, ValueEnum)]
enum OutputFormat {
    Auto,
    Psb,
    Mdf,
}

#[derive(Clone, Copy, Debug, ValueEnum)]
enum MdfWriteShell {
    Auto,
    Plain,
    Mt19937,
}

#[derive(Clone, Copy, Debug, ValueEnum)]
enum MtSeedVariantCli {
    Md5ArrayLe,
    Md5ArrayBe,
    Md5FirstLe,
    Md5FirstBe,
}

#[derive(Clone, Copy, Debug, ValueEnum)]
enum MtKeyVariantCli {
    CyclicLeWords,
    CyclicBeWords,
    StreamLeWords,
    StreamBeWords,
}

#[derive(Clone, Debug, Default, Args)]
struct MdfCliOptions {
    /// Full MT19937 MDF seed, usually key + original file name.
    #[arg(long = "mdf-seed", value_name = "SEED")]
    seed: Option<String>,

    /// MT19937 MDF game key. The CLI combines it with --mdf-name or the input file name.
    #[arg(long = "mdf-key", value_name = "KEY")]
    key: Option<String>,

    /// Original MDF file name used for seed derivation. Defaults to the input file name.
    #[arg(long = "mdf-name", value_name = "NAME")]
    name: Option<String>,

    /// MT19937 MDF key length. FreeMote commonly uses 131 / 0x83.
    #[arg(long = "mdf-key-len", default_value_t = 0x83)]
    key_len: usize,
}

#[derive(Clone, Copy, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "lowercase")]
enum FileKind {
    Psb,
    Mdf,
}

#[derive(Debug, Serialize)]
struct InfoReport {
    path: PathBuf,
    input_kind: FileKind,
    version: u16,
    encrypted: bool,
    checksum: Option<u32>,
    names: TableReport,
    strings: TableReport,
    resources: ResourceReport,
    extra_resources: ResourceReport,
}

#[derive(Debug, Serialize)]
struct TableReport {
    entries: usize,
    bytes: usize,
}

#[derive(Debug, Serialize)]
struct ResourceReport {
    count: usize,
    sizes: Vec<usize>,
}

#[derive(Debug, Serialize, Deserialize)]
struct PackageManifest {
    version: u16,
    encrypted: bool,
    checksum: Option<u32>,
    root: PathBuf,
    resources: Vec<PathBuf>,
    extra_resources: Vec<PathBuf>,
}

